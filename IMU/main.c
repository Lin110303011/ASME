#define MPU9250_ADDR 0x68 << 1

int16_t accelData[3], gyroData[3];
int16_t correctedAccelData[3], correctedGyroData[3];

const int16_t accel_offset[3] = {0, 0, 0};
const int16_t gyro_offset[3] = {0, 0, 0};


void convert_and_correct_data(int16_t* rawData, int16_t* correctedData, const int16_t* offset) {
    correctedData[0] = rawData[0] - offset[0];
    correctedData[1] = rawData[1] - offset[1];
    correctedData[2] = rawData[2] - offset[2];
}

uint8_t MPU9250_ReadReg(uint8_t reg) {
    uint8_t data;
    HAL_I2C_Mem_Read(&hi2c1, MPU9250_ADDR, reg, I2C_MEMADD_SIZE_8BIT, &data, 1, HAL_MAX_DELAY);
    return data;
}

void MPU9250_WriteReg(uint8_t reg, uint8_t data) {
    HAL_I2C_Mem_Write(&hi2c1, MPU9250_ADDR, reg, I2C_MEMADD_SIZE_8BIT, &data, 1, HAL_MAX_DELAY);
}

void MPU9250_Init() {
    MPU9250_WriteReg(0x6B, 0x80);  // Reset MPU9250
    HAL_Delay(100);
    MPU9250_WriteReg(0x6B, 0x00);  // Wake up MPU9250
    MPU9250_WriteReg(0x1C, 0x00);  // Set accelerometer to ±2g
    MPU9250_WriteReg(0x1B, 0x00);  // Set gyroscope to ±250dps
}

void MPU9250_ReadAccelData(int16_t *accelData) {
    uint8_t rawData[6];
    HAL_I2C_Mem_Read(&hi2c1, MPU9250_ADDR, 0x3B, I2C_MEMADD_SIZE_8BIT, rawData, 6, HAL_MAX_DELAY);
    accelData[0] = ((int16_t)rawData[0] << 8) | rawData[1];
    accelData[1] = ((int16_t)rawData[2] << 8) | rawData[3];
    accelData[2] = ((int16_t)rawData[4] << 8) | rawData[5];
}

void MPU9250_ReadGyroData(int16_t *gyroData) {
    uint8_t rawData[6];
    HAL_I2C_Mem_Read(&hi2c1, MPU9250_ADDR, 0x43, I2C_MEMADD_SIZE_8BIT, rawData, 6, HAL_MAX_DELAY);
    gyroData[0] = ((int16_t)rawData[0] << 8) | rawData[1];
    gyroData[1] = ((int16_t)rawData[2] << 8) | rawData[3];
    gyroData[2] = ((int16_t)rawData[4] << 8) | rawData[5];
}

void transmitData(int16_t* accelData, int16_t* gyroData) {
	static char buffer[100];
    int len = snprintf(buffer, sizeof(buffer), "X:%d,Y:%d,Z:%d\r\n",
                       accelData[0], accelData[1], accelData[2]);
    HAL_UART_Transmit(&huart2, (uint8_t *)buffer, len, HAL_MAX_DELAY);
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM2)
  {
    MPU9250_ReadAccelData(accelData);
    MPU9250_ReadGyroData(gyroData);

    convert_and_correct_data(accelData, correctedAccelData, accel_offset);
    convert_and_correct_data(gyroData, correctedGyroData, gyro_offset);

    transmitData(correctedAccelData, correctedGyroData);
  }
}

